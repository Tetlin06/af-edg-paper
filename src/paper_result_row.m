function T = paper_result_row(result, status, message)
%PAPER_RESULT_ROW Convert one result struct into a one-row table.
    if nargin < 2 || isempty(status), status = "ok"; end
    if nargin < 3, message = ""; end

    T = table( ...
        string(result.afdb_id), string(result.pdb_id), string(result.chain_id), ...
        string(result.init_method), double(result.cutoff), double(result.K), ...
        double(result.noise_seed), double(result.n_atoms), double(result.n_ca), ...
        double(result.n_edges), double(result.edge_density_upper), ...
        double(result.af_rmsd_ca), double(result.edg_rmsd_ca), ...
        double(result.edg_gdt_ts), double(result.edg_gdt_ha), double(result.edg_lddt), ...
        double(result.numit), double(result.recon_error), string(status), string(message), ...
        'VariableNames', { ...
            'afdb_id','pdb_id','chain_id','init_method','cutoff','K','noise_seed', ...
            'n_atoms','n_ca','n_edges','edge_density_upper','af_rmsd_ca', ...
            'edg_rmsd_ca','edg_gdt_ts','edg_gdt_ha','edg_lddt', ...
            'numit','recon_error','status','message'});
end
